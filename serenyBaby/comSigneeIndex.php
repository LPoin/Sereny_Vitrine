            Le concept a commencé aux Etats Unis à la fin des années 1970, début des années 1980. <br>
            Joseph Garcia, un interprète en langue des signes américaine, remarque que les enfants entendants de ses amis sourds commencent à communiquer plus tôt. Il en fait donc le sujet de sa thèse et met au point la méthode « Sign with your baby ». <br>
            Parallèlement, Linda Acredolo, psychologue américaine et sa collègue universitaire, Susan Goodwyn, démontrent que les bébés signeurs développent une communication et une capacité cognitive plus rapidement que les bébés non signeurs. Elles créent alors la méthode Baby Signs. En utilisant des signes de l’American Sign Langage qui est alors la 4e langue utilisée aux Etats Unis. <br>
            Le concept n'arrive en France que 10 ans plus tard, par le biais de livres et d’associations.
        </p>
