        <p class="p-3">
            Très tôt, le bébé est capable de se faire comprendre par des signes : il fait au revoir, envoie des bisous... <br> 
            Et il faut attendre encore plusieurs mois avant que Bébé ne puisse parler. En attendant, on peut utiliser des signes issus de la LSF (Langue des Signes Française) à la portée de Bébé et de son entourage. <br> 
            Ainsi les signes permettent d'accompagner et de réduire les pleurs, les colères et les frustrations, de profiter des moments ludiques et paisibles et d'enrichir la relation avec bébé.   
